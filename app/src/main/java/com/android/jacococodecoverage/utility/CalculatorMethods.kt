package com.android.jacococodecoverage.utility

class CalculatorMethods {
    fun add(value1: Int, value2: Int):Int {
        return value1 + value2
    }

    fun subtract(value1: Int, value2: Int):Int {
        return value1 - value2
    }

    fun divide(value1: Int, value2: Int):Int {
        return value1 / value2
    }

    fun multiply(value1: Int, value2: Int):Int {
        return value1 * value2
    }
}